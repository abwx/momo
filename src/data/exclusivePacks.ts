function packForIndex(portraitIndex: number, stem: string): ExclusivePack {
  const ri = ringIdxForPortrait(portraitIndex, stem);
  const narrSlot = portraitIndex >= 0 ? portraitIndex % V.length : ri % V.length;
  const v = V[narrSlot]!;
  const place = PL[ri % PL.length];

  return {
    tagline: pick16(EX_TAG, ri).replace(/\{\{stem\}\}/g, stem),
    stepTitles: pick16(EX_STEP_HEADINGS, ri),
    step1: {
      messages: [
        { from: "partner", text: pick16(EX_P0, ri), delayMs: 400 },
        { from: "partner", text: `（${stem}·${place}）${v.hook}`, delayMs: 650 },
        { from: "partner", text: pick16(EX_P2, ri), delayMs: 550 },
      ],
      choices: [
        { id: "e1a", label: pick16(EX_S1A, ri), delta: { devote: 2, possess: 1 } },
        { id: "e1b", label: pick16(EX_S1B, ri), delta: { boundary: 2, grow: 2 } },
        { id: "e1c", label: pick16(EX_S1C, ri), delta: { grow: 1, devote: 1 } },
      ],
    },
    step2: {
      prompt: v.bin,
      left: { id: "e2L", label: pick16(EX_S2L, ri), delta: { devote: 2, boundary: 1 } },
      right: { id: "e2R", label: pick16(EX_S2R, ri), delta: { possess: 1, grow: 2 } },
    },
    step3: {
      lines: [
        { speaker: "narrator", text: v.tail },
        { speaker: "partner", text: pick16(EX_THANK, ri) },
      ],
      choices: [
        { id: "e3a", label: pick16(EX_S3A, ri), delta: { devote: 2, grow: 1 } },
        { id: "e3b", label: pick16(EX_S3B, ri), delta: { possess: 1, grow: 1 } },
        { id: "e3c", label: pick16(EX_S3C, ri), delta: { boundary: 2, devote: 1 } },
      ],
    },
  };
}

export function getExclusivePack(portraitFile: string): ExclusivePack {
  const i = PORTRAIT_FILES.indexOf(portraitFile as (typeof PORTRAIT_FILES)[number]);
  const stem = portraitFile.replace(/\.png$/i, "").trim() || "对方";
  return packForIndex(i, stem);
}
